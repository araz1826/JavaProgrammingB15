package day10;

public class switchStatement {
    public static void main(String[] args) {
        int number = 2;

        switch(number){
            case 1:
                System.out.println("I love you");
                break;
            case 2:
                System.out.println("I miss you");
                break;
            default:
                System.out.println("I do not know you");
                break;
        }
    }
}
