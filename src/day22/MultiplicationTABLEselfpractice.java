public class MultiplicationTABLEselfpractice {
    public static void main(String[] args) {

        for(int a=1; a<=10; a++){
            System.out.println("Multiplication table of "+ a);
            for(int x=1; x<=10; x++){
                System.out.println(a+ "x"+ x + " = "+ a*x);
            }

        }
    }
}
