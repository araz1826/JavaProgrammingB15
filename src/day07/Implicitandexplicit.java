package day07;

public class Implicitandexplicit {
    public static void main(String[] args) {

        double bigNum = 10;
        int num = (int)12.99;

        long earthToMoon = 10000l;
        int x = (int)earthToMoon;

        System.out.println(bigNum);
        System.out.println(num);
        System.out.println(x);

    }
}
