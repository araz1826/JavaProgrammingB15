package day19.day17;

public class WhilePractice {
    public static void main(String[] args) {
       int x=1;
       while(x<=5){
           System.out.print(x+ " ");
           ++x;
       }
        System.out.println();
       while(x>1){
           --x;
           System.out.print(x+ " ");

       }
    }
}
